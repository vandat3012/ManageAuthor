package com.example.manageauthor.model;
public class Posts {
    private int id;
    private String name;


    public Posts() {
    }

    public Posts(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
